package kr.co.college.bean;

public class StudentBean {
	private String stdNo;		// 학번
	private String stdName;		// 이름
	private String stdHp;		// 휴대폰
	private int stdYear;		// 학년
	private String stdAddress;	// 주소
	
	public String getStdNo() {
		return stdNo;
	}
	public void setStdNo(String stdNo) {
		this.stdNo = stdNo;
	}
	public String getStdName() {
		return stdName;
	}
	public void setStdName(String stdName) {
		this.stdName = stdName;
	}
	public String getStdHp() {
		return stdHp;
	}
	public void setStdHp(String stdHp) {
		this.stdHp = stdHp;
	}
	public int getStdYear() {
		return stdYear;
	}
	public void setStdYear(int stdYear) {
		this.stdYear = stdYear;
	}
	public String getStdAddress() {
		return stdAddress;
	}
	public void setStdAddress(String stdAddress) {
		this.stdAddress = stdAddress;
	}
	
	
	
	
}
